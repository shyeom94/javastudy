package pkg03_compare;

public class MainClass {

  public static void main(String[] args) {
    
    // 비교 연산자 
    // 연산 결과는 boolean 타입 -> true or false
    
    int a = 10;
    int b = 20; 
    
    System.out.println(a > b); // true or false
    System.out.println(a >= b); // true or false
    System.out.println(a < b); // true or false
    System.out.println(a <= b); // true or false
    System.out.println(a == b); // true or false
    System.out.println(a != b); // true or false
    
    // 문자열의 동등 비교는 사용을 금지한다. 
    
    
    
  }
}


